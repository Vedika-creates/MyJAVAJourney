public class Literals {
    public static void main (String a[])
    {

        //Integral Literal
        int x= 23456;
        System.out.println(x);
        //Octal 
        int oct=0145;
        System.out.println(oct);
        //1*8^2+4*8^1+5*8^0
        //Hexadecimal Lateral
        int hexadecimal = 0x12A;
        System.out.println(hexadecimal);
        //binary  Literal
        int binary_lateral=0b1111;
        System.out.println(binary_lateral);



        //floating  Literal
        float f=45.76854f;//need to specify f
        double d =45.0865;//dont need to specify any character
        System.out.println(f);
        System.out.println(d);


        //char  Literal
        char c= 'a';
        char n ='\u0061';
        System.out.println(c);
        System.out.println(n);

        //string  Literal

        String v ="hello vedika-creates";
        System.out.println(v);


        //Boolean  Literal
         boolean j = true;
         boolean k =false;
         System.out.println(j);
         System.out.println(k);


    }
    
}
